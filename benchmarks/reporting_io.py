from __future__ import annotations

import os
import tempfile
from contextlib import suppress
from pathlib import Path
from typing import Any

from reporting_constants import (
    BENCHMARK_INDEX_FILENAME,
    BENCHMARK_REPORT_GENERATED_MARKER,
    SUPPORTED_AGGS,
)
from reporting_documents import (
    benchmark_report_filename,
    format_benchmark_document,
    format_benchmark_index,
    ordered_result_aggs,
)


def render_generated_markdown(body: str) -> str:
    return f"{BENCHMARK_REPORT_GENERATED_MARKER}\n\n{body.rstrip()}\n"


def is_generated_benchmark_report(path: Path) -> bool:
    if path.is_symlink():
        return False
    try:
        return path.read_text(encoding="utf-8").startswith(BENCHMARK_REPORT_GENERATED_MARKER)
    except (OSError, UnicodeDecodeError):
        return False


def validate_report_output_conflicts(output_dir: Path) -> None:
    report_names = [
        BENCHMARK_INDEX_FILENAME,
        *[benchmark_report_filename(agg) for agg in SUPPORTED_AGGS],
    ]
    conflicts = [
        name
        for name in report_names
        if _report_path_exists(output_dir / name)
        and not is_generated_benchmark_report(output_dir / name)
    ]
    if conflicts:
        joined = ", ".join(sorted(conflicts))
        raise ValueError(
            "Output directory contains conflicting benchmark report files that were not "
            "generated by pandas-booster: "
            f"{joined}. Remove those files or choose a different directory."
        )


def write_generated_report(path: Path, content: str) -> None:
    if path.is_symlink():
        raise ValueError(
            "Output directory contains conflicting benchmark report files that were not "
            f"generated by pandas-booster: {path.name}. Remove those files or choose a "
            "different directory."
        )

    temp_name = None
    try:
        with tempfile.NamedTemporaryFile(
            "w",
            encoding="utf-8",
            dir=path.parent,
            prefix=f".{path.name}.",
            suffix=".tmp",
            delete=False,
        ) as handle:
            temp_name = handle.name
            handle.write(content)
        os.replace(temp_name, path)
    finally:
        if temp_name is not None:
            with suppress(FileNotFoundError):
                Path(temp_name).unlink()


def save_results_md(
    results: list[dict],
    stats_evidence: list[dict[str, Any]],
    output_path: str,
    sort_mode: str,
    cardinality: str,
    diagnostic: str,
) -> None:
    output_dir = Path(output_path)
    if output_dir.exists() and not output_dir.is_dir():
        raise ValueError("Markdown benchmark output must be a directory path, not a file path")

    output_dir.mkdir(parents=True, exist_ok=True)
    aggs = ordered_result_aggs(results)
    if not aggs:
        raise ValueError("No benchmark results available to write")

    validate_report_output_conflicts(output_dir)
    expected_report_names = {benchmark_report_filename(agg) for agg in aggs}
    stale_report_names = {
        benchmark_report_filename(agg)
        for agg in SUPPORTED_AGGS
        if benchmark_report_filename(agg) not in expected_report_names
    }
    for stale_report_name in stale_report_names:
        stale_report_path = output_dir / stale_report_name
        if stale_report_path.exists():
            stale_report_path.unlink()

    for agg in aggs:
        agg_results = [result for result in results if result["agg"] == agg]
        agg_evidence = [item for item in stats_evidence if item["agg"] == agg]
        report = format_benchmark_document(
            agg_results,
            agg_evidence,
            sort_mode,
            cardinality,
            diagnostic,
        )
        write_generated_report(
            output_dir / benchmark_report_filename(agg),
            render_generated_markdown(report),
        )

    write_generated_report(
        output_dir / BENCHMARK_INDEX_FILENAME,
        render_generated_markdown(format_benchmark_index(aggs)),
    )
    print(f"\nBenchmark reports saved to: {output_dir}")


def _report_path_exists(path: Path) -> bool:
    return path.exists() or path.is_symlink()
