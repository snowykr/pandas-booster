"""Benchmark report output conflict-handling contract tests."""

from __future__ import annotations

import pytest

from ._report_output_helpers import _generated_markdown, _make_result


def test_save_results_md_removes_stale_generated_aggregation_reports(benchmark_module, tmp_path):
    output_dir = tmp_path / "reports"
    output_dir.mkdir()
    stale_report = output_dir / "mean.md"
    user_note = output_dir / "notes.md"
    stale_report.write_text(_generated_markdown("stale generated report"), encoding="utf-8")
    user_note.write_text("not generated by benchmark.py\n", encoding="utf-8")

    benchmark_module.save_results_md(
        [_make_result(benchmark_module, preset="1key", agg="sum", sort=True)],
        [],
        str(output_dir),
        sort_mode="sorted",
        cardinality="standard",
        diagnostic="none",
    )

    assert sorted(path.name for path in output_dir.glob("*.md")) == [
        "README.md",
        "notes.md",
        "sum.md",
    ]
    assert not stale_report.exists()
    assert user_note.read_text(encoding="utf-8") == "not generated by benchmark.py\n"

def test_save_results_md_rejects_unmanaged_existing_readme(benchmark_module, tmp_path):
    output_dir = tmp_path / "docs"
    output_dir.mkdir()
    readme = output_dir / "README.md"
    readme.write_text("user readme\n", encoding="utf-8")

    with pytest.raises(ValueError, match="conflicting benchmark report files"):
        benchmark_module.save_results_md(
            [_make_result(benchmark_module, preset="1key", agg="sum", sort=True)],
            [],
            str(output_dir),
            sort_mode="sorted",
            cardinality="standard",
            diagnostic="none",
        )

    assert readme.read_text(encoding="utf-8") == "user readme\n"
    assert not (output_dir / "sum.md").exists()

