def test_save_results_md_rejects_generated_report_symlink(benchmark_module, tmp_path):
    output_dir = tmp_path / "docs"
    output_dir.mkdir()
    external_readme = tmp_path / "external-readme.md"
    external_readme.write_text(_generated_markdown("external generated readme"), encoding="utf-8")
    readme = output_dir / "README.md"
    readme.symlink_to(external_readme)

    with pytest.raises(ValueError, match="conflicting benchmark report files"):
        benchmark_module.save_results_md(
            [_make_result(benchmark_module, preset="1key", agg="sum", sort=True)],
            [],
            str(output_dir),
            sort_mode="sorted",
            cardinality="standard",
            diagnostic="none",
        )

    assert readme.is_symlink()
    assert external_readme.read_text(encoding="utf-8") == _generated_markdown(
        "external generated readme"
    )
    assert not (output_dir / "sum.md").exists()

